describe("Cria categoria", () => {
  it("Espero que dois mais dois seja 4", () => {
    const soma = 2 + 2;
    const resultado = 5;

    expect(soma).toBe(resultado);
  });

  it("Espero que dois mais dois nao seja 5", () => {
    const soma = 2 + 2;
    const resultado = 5;

    expect(soma).not.toBe(resultado);
  });
});
